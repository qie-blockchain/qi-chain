function writeJsonToFile(jsonObject, filePath) {
    try {
      const jsonString = JSON.stringify(jsonObject, null, 2); // The third argument (2) specifies the number of spaces to use for indentation.
      fs.writeFileSync(filePath, jsonString);
      console.log(`JSON data has been written to ${filePath}`);
    } catch (error) {
      console.error('Error writing JSON to file:', error);
    }
  }
async function getaValues(){
    let pawsomeAddress = "0x8E2Fe35fad9649AF45E1b4D6D9f0C0ecc1Cf7D8e"
    let mainnetProvider = new ethers.getDefaultProvider(rpcUrls.qiMainnet)
    let holderInfo = {}
    
    let contract = new ethers.Contract(pawsomeAddress, abi, mainnetProvider);

    let totalSupply = await contract.totalSupply()
    for(let i=0; i<totalSupply; i++){
        console.log("calling for the token id", i);
        let holder = await contract.ownerOf(i)
        if(holderInfo.hasOwnProperty(holder)){
            holderInfo[holder]+=1
        }else{
            holderInfo[holder]=1
        }
    }
    const keysArray = Object.keys(holderInfo);
    let sum = 0
    for(let j in keysArray){
        sum += holderInfo[keysArray[j]]
    }
    console.log("the total sum of the NFTs is", sum);
    console.log("done, writing the file");
    writeJsonToFile(holderInfo, "pawsomeHolderInfo")
}

