async function interactWithContract(contractAddress, abi, providerUrl) {
  // Connect to Ethereum node
  const provider = new ethers.JsonRpcProvider(providerUrl);

  // Load contract ABI and address
  const contract = new ethers.Contract(contractAddress, abi, provider);
  // Get all public methods of the contract
 
    const methods = ["accTokenPerShare", "rewardPerBlock", "totalStaked", "totalUsers", "availableRewardTokens"]
  // Prepare an object to store method results
  const results = {};

  // Call each public method and store the result
  for (const method of methods) {
    try {
      const result = await contract[method]();
      results[method] = result.toString();
    } catch (error) {
      results[method] = `Error: ${error.message}`;
    }
  }
  const constructorValuesMethods = ["rewardPerBlock", "depositFee", "withdrawFee", "rewardToken", "poolLimitPerUser"]
  const constructorResults = {}
  for (const method of constructorValuesMethods) {
    try {
      const constructorResult = await contract[method]();
      constructorResults[method] = constructorResult.toString();
    } catch (error) {
        constructorResults[method] = `Error: ${error.message}`;
    }
  }
  const mergedDict = {
    "publicVars":results,
    "constructorArgs":constructorResults

  }
  // Save results in a JSON file
  
  fs.writeFileSync(outputFilename, JSON.stringify(mergedDict, null, 2));

  console.log(`Results saved in ${outputFilename}`);
}

